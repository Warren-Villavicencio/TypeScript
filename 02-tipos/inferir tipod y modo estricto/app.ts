(() => {
    // Esta parte crea una función anónima. La flecha `=>` indica que es una función flecha.
    // Los paréntesis vacíos `()` significan que la función no recibe ningún parámetro.

    const a = 10; // Declaramos una constante llamada 'a' y le asignamos el valor 10.
    console.log(a); // Imprimimos el valor de 'a' en la consola, que será 10.

})(); // Aquí invocamos inmediatamente la función que acabamos de crear.
       // Los paréntesis adicionales al final hacen que se ejecute en este mismo momento.

